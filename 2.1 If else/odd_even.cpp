#include<iostream>
using namespace std;
int main()
{
    int n;
    cout<< "Enter any Number to check Even or Odd" << endl;
    cin>>n;
    cout<< "Entered no is:" << n << endl;
    if(n%2==0)
    {
        cout<< "Entered no is Even" << endl;

    }
    else {
        cout << "Entered no is Odd" << endl;

    }
}
